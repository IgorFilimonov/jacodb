function primitiveLiteralsUsage() {
    console.log('5' + 3);          // "53" (конкатенация строки и числа)
    console.log('5' - 3);          // 2 (строка '5' преобразуется в число 5)
    console.log('5' * 3);          // 15 (строка '5' преобразуется в число 5)
    console.log('5' / 2);          // 2.5 (строка '5' преобразуется в число 5)
    console.log(true + 1);         // 2 (логическое значение true преобразуется в число 1)
    console.log(false + 1);        // 1 (логическое значение false преобразуется в число 0)
    console.log(null + 1);         // 1 (null преобразуется в число 0)
    console.log(undefined + 1);    // NaN (undefined не преобразуется в число, результат - NaN)
    console.log('5' == 5);         // true (строка '5' преобразуется в число 5 для сравнения)
    console.log(null == undefined); // true (null и undefined считаются равными)
}

function complexLiteralsUsage() {
    console.log([] + 1);           // "1" (пустой массив преобразуется в пустую строку, результат - конкатенация)
    console.log([1] + 1);          // "11" (массив с одним элементом преобразуется в строку, результат - конкатенация)
    console.log([1, 2] + 1);       // "1,21" (массив преобразуется в строку, результат - конкатенация)
    console.log({} + 1);           // "[object Object]1" (объект преобразуется в строку, результат - конкатенация)
}
